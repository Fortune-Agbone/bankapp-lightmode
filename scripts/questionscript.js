//script for frequently asked questions answer for box one
var boxStatus1 = 0

$(function () {
    $('.questionboxclick1').click(function () {
        ++boxStatus1;
        if (boxStatus1 % 2 === 0) {
            $('.infobox1').slideUp(300);
        }
        else {
            $('.infobox1').slideDown(300);
        }
    })
})
//end of answer box one script


